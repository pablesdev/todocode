
package ejercicioifelse;

public class EjercicioIfElse {

    public static void main(String[] args) {
        int num1 = 10;
        int num2 = 10;
        
        if(num2 > num1){
            System.out.println("El num2 es mayor al num1");
        } else if (num2 < num1){
            System.out.println("El num1 es mayor al num2");
        } else{
            System.out.println("El num1 es igual al num2");
        }
    }
}