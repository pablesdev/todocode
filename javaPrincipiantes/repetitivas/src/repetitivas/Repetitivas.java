
package repetitivas;

public class Repetitivas {

    public static void main(String[] args) {
        
        // Ejemplo While
        
        int cont = 0;
        
        while (cont <=10){
            System.out.println("Estoy en la vuelta "+cont+" con While");
            cont++;
        }
        
        //Ejemplo For
        
        for(int conta=0;conta<=10;conta++){
            System.out.println("Estoy en la vuelta "+conta+" con For");
            
        }
        
    }
    
}
